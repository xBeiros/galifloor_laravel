<script setup lang="ts">
import { computed } from 'vue';

const emit = defineEmits(['update:checked']);

const props = defineProps<{
    checked: boolean;
    value?: any;
}>();

const proxyChecked = computed({
    get() {
        return props.checked;
    },

    set(val) {
        emit('update:checked', val);
    },
});
</script>

<template>
    <input
        type="checkbox"
        :value="value"
        v-model="proxyChecked"
        class="rounded border-gray-300 dark:border-gray-600 text-indigo-600 dark:text-indigo-400 bg-white dark:bg-gray-700 shadow-sm focus:ring-indigo-500 dark:focus:ring-indigo-400"
    />
</template>
